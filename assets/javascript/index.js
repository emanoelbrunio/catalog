let divPersons = document.querySelector('.persons');
function person(i, name, nameSerie) {
    return (
        `
        <img src="assets/imagens/persons/p${i}.jpeg" alt="">
        <div class="divNames">
            <h4 class="name">${name}</h4>
            <h4 class="nameSerie">${nameSerie}</h4>
        </div>
        `
    )
}

